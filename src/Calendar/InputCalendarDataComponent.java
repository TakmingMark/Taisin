package Calendar;


import java.util.List;

public class InputCalendarDataComponent {
	private List<String> yearList;
	private List<String> monthList;
	private List<String> dayList;
	private List<String> hourList;
	private List<String> minuteList;
	private List<String> secondList;
	
	public List<String> getYearList() {
		return yearList;
	}
	public List<String> getMonthList() {
		return monthList;
	}
	public List<String> getDayList() {
		return dayList;
	}
	public List<String> getHourList() {
		return hourList;
	}
	public List<String> getMinuteList() {
		return minuteList;
	}
	public List<String> getSecondList() {
		return secondList;
	}
	public void setYearList(List<String> yearList) {
		this.yearList = yearList;
	}
	public void setMonthList(List<String> monthList) {
		this.monthList = monthList;
	}
	public void setDayList(List<String> dayList) {
		this.dayList = dayList;
	}
	public void setHourList(List<String> hourList) {
		this.hourList = hourList;
	}
	public void setMinuteList(List<String> minuteList) {
		this.minuteList = minuteList;
	}
	public void setSecondList(List<String> secondList) {
		this.secondList = secondList;
	}
	

}
